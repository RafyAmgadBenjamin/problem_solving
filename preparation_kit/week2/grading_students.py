def gradingStudents(grades):
    # Write your code here
    rounded_grades = []
    for grade in grades:
        if grade >= 38:
            divsib = int(grade/5)
            next_div = divsib + 1 
            seil = next_div * 5 
            if seil - grade < 3:
                grade = seil
        rounded_grades.append(grade)
    return rounded_grades


